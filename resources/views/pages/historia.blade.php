            <section class="page-section">
                <h2 class="section-title">
                    1925-1926 La Fundació
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            Es constitueix la “Sociedad la Alianza” el 22 d’octubre de 1925, a les 4 de la tarda, al local
                            de La Sagrera.
                        </p>
                        <p>
                            La finalitat de la “Sociedad” queda definida com a : “fines puramente morales y de carácter
                            cultural”. La junta està constituïda per un president i 3 vocals.</p>
                        <p>
                            El 18 de febrer de 1926 es comença a parlar de la construcció d’un edifici i de l’existència
                            d’uns terrenys idonis, valorats en 10.000 ptes., els quals es decideix comprar. L’Aliança
                            realitza un préstec de 40.000 ptes. emetent a tal objecte 800 obligacions nominatives de 50
                            ptes. cada una, sense interès, amortizables per sorteig en 160 anys, a raó de 5 obligacions per
                            any.
                        </p>
                        <p>
                            Es decideix no muntar envelat per la Festa Major i inaugurar el nou local el setembre del
                            1926.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1927-1931 La Construcció
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            Les obres del nou edifici tenen un cost més elevat, cosa que provoca que una sèrie de socis,
                            anomenats “socis protectors”, paguin el que resta, import que els serà retornat en diferents
                            pagaments per la mateixa societat. D’altra banda, també s’augmenta la quota de socis de 60
                            cèntims a 1 pesseta.</p>
                        <p>
                            El setembre de 1927 el constructor, Josep Fargas Roca, reclama a l’Aliança 66.805, 40 pessetes
                            en motiu de la construcció de l’edifici, quantitat que els socis s’oposen a pagar.</p>
                        <p>
                            El juliol de 1928 es dicta sentència: l’Aliança ha de pagar una quantitat menor a la reclamada.
                            Per primer cop, el gener de 1929, l’Aliança disposarà d’un conserge, el senyor Manuel Fonoll.
                        </p>
                        <p>
                            És l’any en què es comença a parlar de “socis balladors” (socis en època de Festa Major) i
                            “socis protectors” (socis amb possessió d’obligacions).
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1931 – 1939 Temps difícils
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            L’any 1931 es produeix la polèmica sobre si l’orquestra que es contracta per la Festa Major ha
                            d’anar a tocar a l’església, com era costum fins aleshores. Després d’una votació secreta (44 a
                            12), s’acorda que l’orquestra no tocarà a l’església, cosa que fins i tot provocarà dimissions
                            en la Junta.
                        </p>
                        <p>
                            S’acorda, el 1932, prorrogar la quota de Festa Major: els socis balladors, 30 ptes; les llotges,
                            40 ptes., i les cadires, 4 ptes., quotes que l’any 1933 es reduiran pels socis balladors a 20
                            ptes.</p>
                        <p>
                            El 27 de gener de 1934 es redacta la primera acta en català i es continua així fins que esclata
                            la Guerra Civil.
                        </p>
                        <p>
                            “Seguidament el President eposa que se tindria que votar president y sis vocals, pero que
                            mentres durin les actuals circumstancies davant la falta de jovent ell seguiria en el mateix
                            lloc” redactat de l’any 1938 que descriu les circumstàncies d’aquella èpoc a la Sociaedad La
                            Alianza.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1940-1945 Les llotges
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            L’any 1940, després d’un any d’inactivitat, es tanca l’acta amb el següent text “Una vez tomado
                            posesión de sus cargos se acordó levantar la sesión haciendo votos por la prosperidad de la
                            Sociedad, de España y su Caudillo”.
                        </p>
                        <p>
                            Aquest mateix any s’acorda la construcció d’una galeria de llotges al primer pis, les quals
                            s’arrendaran per sis anys a un preu de 250 ptes. la llotja.
                        <p>
                            Els nous Estatuts de la Societat es constitueixen l’any 1941, en ser aprovats pel Governador
                            Civil de la Província. Aquesta mateixa acta acaba amb la frase “Haciendo votos por la nueva
                            España y con los gritos de costumbre de Arriba España, Viva Franco, se levanta la sesión”.
                            Donada la gran afluència a la Festa Major, es veu necessari, l’any 1943 construir cinc llotges
                            més desmontables, de fusta. És aquest any quan es contracta el primer “adornista” per engalanar
                            la sala per la Festa Major.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1946-1950 Noves reformes
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            Aquest període comença amb diferents reformes: l’any 1946 es pinta la sala i el 1947 es fan
                            reformes al bar “pintar el café, puertas y ventanas del mismo, instalación de bancos de madera
                            situados junto a la pared con el fín de ahorrar cierto número de sillas y mejor comodidad”.
                        </p>
                        <p>
                            El juny de 1950 es decideix comprar la màquina de cinema sonor instal·lada a la sala, de la
                            mateixa manera que s’adquireix a l'”Empresa de Cine Victoria”, de Parets del Vallès, el
                            transformador “Lámpara Neón”.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1951-1955 Comença el ball…
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            És el 1951 quan un grup de socis s’interessa per habilitar el pati, convertint-lo en un camp de
                            bàsquet.</p>
                        <p>
                            S’aprova, el 5 de març de 1952, aixecar un envà a l’estatge del conserge, per la qual cosa es
                            guanya una habitació i un lavabo. De la mateixa manera, s’acorda reparar la teulada de la sala,
                            donada la precarietat amb què es trobava.</p>
                        <p>
                            La proposta de fer ball un cop al mes sorgeix a l’abril de 1954 quan un grup de socis demanen
                            “un medio de diversión”, cosa que representa augmentar la quota de socis 14 pessetes per tal
                            d’assumir els costos de l’activitat.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1956-1960 Crisi econòmica
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            L’inici de 1956 ve marcat per una forta crisi econòmica : l’Aliançaa s’ha de fer càrrec de dues
                            hipoteques per import de 120.000 ptes. Després d’una llarga discussió de com amortitzar el
                            deute, sorgeix la següent idea “los señores asistentes acuerdan por unanimidad se solicite del
                            magnífico Ayuntamiento se haga cargo de dichas hipotecas previo acuerdo con los señores
                            tenedores de las Obligaciones”, solució que mai no arriba.</p>
                        <p>
                            Serà el febrer d’aquest mateix any quan la Sociedad La Alianza rep el permís d’obertura del
                            cinema i el 1957 es reforma la cabina cinematogràfica “por disposición del Excelentísimo Señor
                            Gobeernador Civil”.</p>
                        <p>
                            El primer reglament de lloguer de sala data del 4 de febrer de 1958, quan s’acorda que qualsevol
                            acte se celebri a la sala per alguna entitat aliena a l’Aliança haurà de pagar 100 pessetes “en
                            concepto de desgaste del salón”.</p>
                        <p>
                            El 25 de desembre de 1960 els dècims de la loteria de l’Aliança són premiats en 5 ptes. més el
                            reintegrament cada un.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1961-1965 Millores en l’edifici
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            El juny de 1962 s’aproven diferents projectes : adquisició de cent-cinquanta cadires,
                            construcció d’un local per resguardar les motos i les bicicletes, substitució del teló i
                            renovació total de l’edifici.</p>
                        <p>
                            El gener de 1963 es comença a instal·lar la calefacció de la sala, la qual no entra en
                            funcionament total fins l’any 1966.</p>
                        <p>
                            Es demanen pressupostos per tal de “rebozar y estocar con arenilla de mármol las fachadas:
                            principal y lateral del campo de balonmano” l’abril de 1965. Les obres seran otorgades a Antonio
                            Olivan. Per tal d’assumir el cost de les mateixes la quota de socis augmentarà a 750 ptes. (un
                            cent per cent).
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1966-1972 Consolidació del patrimoni
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            El 2 de gener de 1966 s’aprova l’obertura del carrer “Avenida José Antonio” i l’aixecament de
                            les clavegueres, de la mateixa manera que s’aprova cancelar la hipoteca per un valor de 80.000
                            ptes., hipoteca que datava de 1931.</p>
                        <p>
                            Tanmateix, i en el mateix any, s’aprova l’adaptació dels estatuts a la nova llei d’associacions.
                            Marcel·lí Mazarico, fins aleshores conserge i administrador del cafè-bar, anuncia el 5 de
                            desembre de 1968 el cessament del seu càrrec, que deixarà definitivament el 31 de març de 1969.
                            S’obre a concurs públic la concessió del bar, que assumirà Josep Bach.</p>
                        <p>
                            L’any 1969 es reclama la compra d’una taula de ping-pong i l’acondicionament d’un espai pel
                            correcte desenvolupament d’aquest esport.</p>
                        <p>
                            La Festa Major de 1971 fou un èxit total, ja que tingué lloc l’actuació de “La Trinca”.
                            L’abril de 1972 s’aprova el pressupost per la construcció d’una pista poliesportiva, valorada en
                            947.000 ptes.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1973 La Pista és una realitat
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            Els projectes de l’Aliança es van multiplicant gràcies a la quantitat d’activitats que aquesta
                            absorbeix: reforma de la sala d’espectacles, construcció d’una sala a part per la pràctica del
                            ping-pong i la col·locació d’una separació al bar amb la qual s’aconsegueixi que els socis
                            tinguin una àmplia zona reservada per a ells.</p>
                        <p>
                            Aquest mateix any s’inaugura la pista poliesportiva amb un munt d’activitats i actes, on totes
                            les seccions esportives de l’Aliança hi són representades.</p>
                        <p>
                            Donat el gran nombre d’usuaris de la pista, es sol·licita als socis “colaboración para organizar
                            las secciones deportivas y se requiere a personas que voluntaria y desinteresadamente se
                            coloquen al frente de las mismas junto con el delegado de deportes de la Junta Directiva”.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1974-1977 El Cinquantenari
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            El replanteig de reforma de la sala, que té lloc al 1974, porta l’Aliança a demanar un préstec
                            de 4.000.000 ptes. a la “Caja de Pensiones para la Vejez y Ahorros” a retornar en 16 anys.
                            El 30 de juny de 1974, i per primera vegada en la història de la societat, es dóna entrada com a
                            secretari a una dona, Carme Prims i Colomé.</p>
                        <p>
                            A l’abril de 1975 es tornen a escriure les actes en català.
                            El 1976 té lloc la celebració del Cinquantenari de l’Aliança. Per aquest fet es creen diverses
                            comissions que es cuiden d’organitzar les diferents activitats, entre elles la realització de la
                            medalla anomenada “Licinius”, de Subirachs.</p>
                        <p>
                            Aquest mateix any l’arrendament del cafè-bar passa a la senyora Antònia Tussell.
                            El 7 de magi de 1977 i arrel de la lectura d’una carta enviada per l’Assamblea Democràtica de
                            Lliçà d’Amunt i Santa Eulàlia de Ronçana, l’Aliança decideix adherir-se a la campanya “Volem
                            l’Estatut” engegada per l’Assemblea de Catalunya.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1978-1979 Aliança i política
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            Ja són tres els equips que l’Aliança té federats, per la qual cosa, a l’octubre de 1978, es
                            demana col·laboracio econòmica per part de l’Aliança, ja que aquests són incapaços
                            d’autofinançar-se. Tanmateix, és el mes en què neix la proposta de muntar un gimnàs a la sala de
                            ping-pong.</p>
                        <p>
                            El 3 de novembre del mateix any es convoca una assemblea extraordinària, donat que l’Associació
                            de Sant Julià demana la sala per tal de realitzar una sessió informativa, sessió que per a uns
                            quants socis és de caire polític. La Junta, després d’escriure un manifest a favor de la
                            realització de la sessió, decideix posar a votació la qüestió: 73 vots favor, 69 en contra, 5
                            nuls i 4 en blanc.</p>
                        <p>
                            Serà el 1979 quan s’aprova un nou preu de lloguer per la sala, 6.000 ptes., destinades a cobrir
                            les despeses generals que la utilització de la mateixa comporta.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1980-1981 Festa Major conjunta
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            La Festa Major de 1980 es comença a organitzar conjuntament amb l’Ajuntament, el qual cedeix una
                            quantitat de diners per tal de finançar un “20% sobre el taquillatge dels actes organitzats en
                            el local de la societat, al temps que vol que tothom pagui l’entrada igual”. Així mateix va dir
                            que “dels actes que organitza l’Aliança no subvencionarà cap cost; com a contrapartida es
                            compromet a col·laborar per una programació extraordinària amb una quantitat mínima de 100.000
                            ptes. i com a màxim de 200.000 ptes”.</p>
                        <p>
                            Aquest mateix any s’aprova la no continuïtat del cinema, que fins ara es feia cada quinze dies,
                            donada la impossibilitat de canviar la programació amb el cine-club de Caldes, del qual es
                            depenia. D’altra banda, es notifica l’inici d’una programació quinzenal de cinema infantil.</p>
                        <p>
                            Les seccions informen:</p>
                        <p>
                            Futbol Sala i Bàsquet femení: “El balanç global és satisfactori”. S’apunta la possibilitat de
                            fer un equip de mini-bàsquet.</p>
                        <p>
                            Ping-pong: s’inicia una roda d’entrenaments infantils per tal de donar continuïtat a la secció.
                            Els ingressos de la secció es veuen afavorits per la loteria de Nadal.</p>
                        <p>
                            Sardanes: es presenten moltes dificultats de tipus econòmic, ja que un programa d’actuacions al
                            llarg de la temporada es costós i deficitari. Es fa una crida per la col·laboració dels socis.
                            Coral: s’informa del procés seguit per la seva creació.</p>
                        <p>
                            Escacs: la secció presenta una manca de participants, de manera que es prenen un descans. Tot i
                            així es realitza un curset per a infants.</p>
                        <p>
                            Cinema infantil: èxit rotund, ja que aquesta secció omple el buit que hi ha al poble pel que fa
                            a activitats lúdiques infantils.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1982 Continuïtat
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            La Festa Major es continua organitzant amb col·laboració de l’Ajuntament i l’Aliança assumeix un
                            30% del dèficit global d’aquesta festa, fins un límit de 250.000 ptes. Per altra banda,
                            l’Aliança cedeix la sala d’actes i es fa càrrec del seu manteniment, així com de les despeses de
                            porteria i de guixeta.</p>
                        <p>
                            La Festa Major de 1982 es caracteritza per ser el primer any que actua a Lliçà d’Amunt
                            l’orquestra Maravella, la qual provocarà bona part del dèficit, degut al seu elevat cost.</p>
                        <p>
                            El 14 de novembre visita les instal·lacions de l’Aliança el Conseller de Cultura de la
                            Generalitat de Catalunya, Max Canher.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1983 Captació de socis
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            El vicepresident informa de la nova empenta que s’ha volgut donar desde l’Aliança a la Festa de
                            Sant Antoni. També parla de l’èxit de la primera mostra de bestiar i de la nova activitat que es
                            posarà en marxa el segon dissabte del proper mes, els Balls de Saló.</p>
                        <p>
                            Tanmateix, en Jordi Casanova ens comunica la formació d’una nova coral, la Coral Infantil, que
                            ja ha començat a actuar per les Festes de Nadal en les representacions dels Pastorets.</p>
                        <p>
                            Es proposa, per al mes de juny, una campanya per a ampliar el nombre de socis. Per reforçar
                            aquesta campanya es proposa actualitzar i “renovar -en català- tots els carnets de soci i que
                            els no socis hagin de pagar un 50% més en tots els actes, llevat els de la Festa Major i els
                            d’entrada gratuïta”.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1984 La Festa Major a l’envelat
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            “Festa Major: Envelat?”</p>
                        <p>
                            L’Ajuntament decideix la incorporació d’un envelat a “aquesta festa tant tradicionalment
                            nostra”, ja que la capacitat de convocatòria d’assistents està per sobre les capacitats reals de
                            la sala. La Junta “ha intentat demostrar amb xifres que les despeses a fons perdut de lloguer i
                            acondiciar cada any l’envelat són suficients per finançar l’engrandiment de la sala”.</p>
                        <p>
                            Després d’una votació secreta queda acceptada la proposta municipal, sempre que l’Aliança pagui
                            com a màxim 300.000 ptes. en concepte d’entrades pels seus socis.</p>
                        <p>
                            El 8 de setembre, l’Aliança rep la visita del President de la Generalitat de Catalunya, Jordi
                            Pujol i Soley, el qual escriu: “Felicito l’Aliança de Lliçà d’Amunt perquè durant molts anys ha
                            estat un element bonic de vertebració del poble, i l’encoratjo a seguir-ho fen amb esperit de
                            voluntariat”.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1985 Impuls a la Festa Anyal
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            Comencen a funcionar els nous vestuaris de la pista poliesportiva.</p>
                        <p>
                            Es millora l’aspecte del cafè-bar amb un mosaic nou, es tiren a terra els lavabos vells del
                            cafè-bar i se’n posen uns de nous “i més moderns”, i es construeix una sala adicional a la de
                            tennis-taula i, a sobre dels vestuaris, una de petita.</p>
                        <p>
                            S’expressa la voluntat de donar un nou impuls a la Festa Anyal que es realitza el mes de
                            novembre, en la qual tots els socis gaudeixen de l’entrada gratuïta.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1986 Aliança per a totes les edats
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Es reprèn l’activitat de “ball amb discs” per al jovent, ja que es considera molt interessant
                            perquè aquests participin en les activitats que es realitzen a l’Aliança.</p>
                        <p>
                            El 5 de setembre de 1986, en el decurs de la Festa d’Homenatge a la Vellesa de la Festa Major,
                            “ens honra amb la seva presència” el President del Parlament de Catalunya, Miquel Coll: “M’és
                            molt grat de fer constar ací la meva més cordial i sincera felicitació a l’Aliança i als seus
                            dirigents i socis per la tasca que porten a terme i desitjant-los que continuïn fent-la en forma
                            cada vegada més satisfactòria”.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1987 Modificació dels Estatuts

                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>
                            El 7 de març s’informa de la propera obertura de l’activitat de ball amb disc els diumenges a la
                            tarda, l’anomenada Disc-7.</p>
                        <p>
                            Continua havent un gran assistència de balladors als Balls de Saló, malgrat que el nombre de
                            socis assistents és força baix.</p>
                        <p>
                            Es proposa que l’espai que queda darrera l’escenari que ha quedat tancat amb la separació del
                            carrer, es destini a fer-hi unes pistes de petanca.</p>
                        <p>
                            Tanmateix, es discuteix el funcionament del nou gimnàs, que passarà a ser una secció més de
                            l’Aliança.</p>
                        <p>
                            La nova Junta proposa una sèrie de modificacions dels estatuts per tal de donar-los “més
                            funcionalitat i millor utilitat a l’hora d’establir les competències i afanys de l’entitat,
                            davant de coorporacions locals i altres estaments de govern estatal i/o autonòmic”.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1988 Consolidació dels nous espais
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>La Disc-7 té un gran acolliment al poble, sobretot entre els cercles de jovent, i esdevé el lloc
                            de trobada dels diumenges a la tarda.</p>
                        <p>
                            Els Balls de Saló continuen tenint una gran assistència de públic de totes les edats i es
                            converteixen en l’activitat lúdica dels segons dissabtes de cada mes.</p>
                        <p>
                            Durant l’any els espais creats en les darreres reformes (Gimnàs, Escola de Música, etc…)
                            assoleixen una gran utilitat i donen lloc a una major incorporació de nous socis.</p>
                        <p>
                            Gracies a l’Escola de Música de l’Aliança cada vegada és més la gent que aprèn algun instrument
                            al poble de Lliçà, sobretot nens i nenes.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1989 Noves propostes
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Són molts els projectes que la Junta es planteja dur a terme durant aquest any: acabar el
                            projecte de seguretat, instal·lació de llum nocturn, instal·lar un nou telèfon al bar i mantenir
                            el mateix per a l’entitat, pintar tanques publicitàries a la paret exterior de la pista, comprar
                            cadires noves, netejar les cortines de l’escenari, passar els rebuts per ordinador, i es
                            planteja la possibilitat de contractar un conserge que s’encarregui del manteniment general del
                            local (posar i treure cadires, tancar i obrir portes…).

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1990 Reconeixement a una tasca
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>En motiu del 65è aniversari de l’Aliança, la Federació d’Ateneus de Catalunya fa entrega d’un
                            diplom el 7 d’octubre de 1990.</p>
                        <p>
                            Es proposa arreglar la sala de juntes per tal d’habilitar-la com a secretaria i oficina de
                            l’entitat, de la mateixa manera que s’adquireix un ordinador per tal d’informatitzar totes les
                            dades dels socis per elaborar els rebuts anuals, per la qual cosa aquests són degudament
                            informats.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1991 Moments complicats

                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Es presenta una preocupació per la renovació de la nova Junta, quedant composada la mateixa per
                            tres persones, presidenta i dos vocals. Es planteja la necessitat de convocar una altra
                            assemblea de socis per l’elecció de nous membres. La Junta actual, amb les seccions, es planteja
                            seriosament la continuïtat de l’Aliança, ja que la presidenta, Maria de Riba, no pot continuar
                            assumint tots els càrrecs de la Junta Directiva amb les responsabilitats que comporta.</p>
                        <p>
                            Es convoca una sèrie de socis, considerats d’interès per l’Ateneu, dels quals sorgeix la nova
                            Junta.</p>
                        <p>
                            És en aquest any quan sorgirà el primer número de la nova revista de l’Aliança, la qual consta
                            d’una editorial, informació sobre l’Ateneu i un reportatge de cada secció.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1992-1993 Ressorgiment necessari

                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>L’any 1992 va ser un any de transició, amb un daltabaix a l’entitat molt important i en el qual
                            el futur de l’Ateneu va arribar a ser incert.</p>
                        <p>
                            El 1993 entra una nova Junta amb renovades il·lusions i projectes, la qual es marca com objectiu
                            motivar als socis i, molt especialment, a les seccions, per tal de crear una bona dinàmica
                            associativa. En aquest sentit es realitzen actes com el “Sopar de les Seccions”, la “Festa
                            Anyal”, el “Carnestoltes” i el “Primer Joc de Pistes” i es continuen les activitats ja
                            iniciades, com els Balls de Saló. Es busca la col·laboració de les diferents secccions, perquè a
                            més de les actiitats pròpies que realitzen regularment, se sentin integrades cada vegada més en
                            el conjunt de l’entitat.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1994 Noves activitats

                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Seguint la línia marcada de l’any anterior, aquest any també és molt remarcable a nivell
                            d’activitats, essent aquestes molt variades per tal de poder arribar al major nombre de públic
                            possible, la qual cosa provoca, al mes de setembre, un increment en el nombre de socis.</p>
                        <p>
                            És enguany quan s’inicia l'”Aliansalsa”, la qual es realitza els divendres de les 11 de la nit
                            fins les 4 de la matinada, amb programacions com “Lucrecia y su grupo”.</p>
                        <p>
                            La calçotada i la passejada en bici també caracteritzen aquest 1994.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1995 Inici de la Juguesca

                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Aquest any es caracteritza per l’inici d’activitats que tindran una gran acollida municipal. El
                            premi literari “Lletraferit”, inicia l’any 1993, passarà a formar part dels actes de Sant Jordi,
                            realitzant-se bianualment.</p>
                        <p>
                            Els concerts de rock tindran cada vegada una assistència més elevada, portant així grups com
                            “Murphies”, “Coda”,…</p>
                        <p>
                            Finalment, el 1995 ve marcat pel primer de “La Juguesca”, tot un seguit de proves d’habilitat,
                            força i enginy que realitzen una sèrie de grups, compostos per més d’un centenar de persones, i
                            que s’aniran posicionant com a actes centrals de la Festa Major: construcció de castells de
                            sorra, castells humans i la mítica prova de l’estirada de tractor, són algunes de les proves que
                            es realitzen aquell any. La “Juguesca” ha estat tota una revolució en la Festa Major de Lliçà
                            d’Amunt.

                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1996 Bon ritme d’activitats
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Cada cop són més les activitats que s’organitzen des de l’Aliança per tal d’arribar al major
                            nombre de públic possible, i per tal que aquest sigui el més divers.</p>
                        <p>
                            Així, trobem com l’Aliança s’encarrega de les Festes de Nadal i de Cap d’Any; del Carnaval, ja
                            sigui aquest infantil o per a gent gran; de Sant Jordi i del Lletraferit; la Nit de Càtars ; de
                            Sant Joan; de concerts de rock, així com d’un cicle d’activitats infantils, sense oblidar la
                            mítica “Juguesca” de Festa Major.</p>
                        <p>
                            Els guanyadors de “La Juguesca” organitzen un dinar, on hi són convidades totes les colles i
                            l’organització per menjar-se el porc que hi ha com a premi pels vencedors.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1997 Activitats diverses
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Es mantenen les seccions de gimnàs, karate, billar, tennis taula, bàsquet, cant coral, cor de
                            Clavé, futbol sala i sevillanes, totes elles portant el mateix ritme d’actuació que l’any 1996.
                            D’altra banda, la comissió de cultura, que compta ja amb tres anys d’existència, programa
                            activitats molt diverses: 4ª calçotada, carnaval, la festa de Sant Jordi i la Festa Anyal (amb
                            la Primera Mostra de Cargols), concerts de rock, la Transegre i la 2ª Tapadalejada (de Granera a
                            Lliça).</p>
                        <p>
                            L’Escola de Música celebra el seu 10è Aniversari amb un augment constant d’alumnes inscrits.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1998 La sala canvia d’imatge
                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>El ritme d’activitats es manté i fins i tot augmenta. La “Juguesca” “compta ja amb la
                            participació de cinc colles diferents: els Concos (guanyadors de les 3 edicions anteriors), els
                            Can t’implora (segon en les tres edicions), els Skando, els Spòrting l’Olla i els Bisky (primer
                            any de participació).</p>
                        <p>
                            Els concerts de rock es troben en un moment àlgid, comptant amb les actuacions d’Ismael Serrano,
                            Màrtires del Compàs, Mercedes Ferrer i la participació dels DJ Showarma i Falafel, que amenitzen
                            el final de tots aquests.</p>
                        <p>
                            Comencen les obres que donaran una imatge diferent a la sala, s’arrenca la mítica moqueta que
                            cobreix les parets i es fa la nova instal·lació elèctrica.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    1999 Premi de la Federació

                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>Es guanya el premi de la Federació d’Ateneus a l’activitat sòcio-cultural que fomenta la
                            participació popular: la “Juguesca”.</p>
                        <p>
                            Pel que fa a les activitats, aquests continuen amb un gran nombre de participació, menys el
                            cicle de teatre, que es suspèn per ser deficitari.</p>
                        <p>
                            El Sant Jordi de 1999 es caracteritza per ser el primer any en què tindrà lloc al carrer de
                            l’Aliança el primer Bram del Drac, una actuació musical composada pel director argentí Marcelo
                            Bellagamba amb l’ajuda d’en Lluís Olivan i interpretada per la gent del poble, de la qual
                            sorgeix el primer disc compacte de les cançons representades.
                        </p>
                    </div>
                </div>
            </section>
            <section class="page-section">
                <h2 class="section-title">
                    2000 75 anys de l’Aliança

                </h2>
                <div class="section-content row">
                    <div class="col-12">
                        <p>S’intenta crear una comissió per tal de programar tot un conjunt d’activitats lúdiques i culurals
                            obertes a tothom en motiu de la commemoració de 75è aniversari, i es procur que aquestes es
                            donin a conèixer a tota la població de Lliçà d’Amunt.</p>
                        <p>
                            El Bram del Drac, espectacle musical basat en la llegenda de Sant Jordi, es realitza a la Plaça
                            de l’Església. En aquesta nova ubicació, amb un gran nombre de públic assistent i una posada en
                            escena molt acurada, rep grans elogis i molt bones crítiques.</p>
                        <p>
                            També hi ha gran assistència a la cremada del Drac.</p>
                        <p>
                            El Drac és construït amb la col·laboració de les escoles d’infantil, primària i IES.
                        </p>
                    </div>
                </div>
            </section>

